    texts = [example['question'] for example in batch]
    images = [example['image'].convert("RGB") for example in batch]
    labels = [example['answer'] for example in batch]

    inputs = processor(
        text=texts,
        images=images,
        suffix=labels,
        return_tensors="pt",
        padding="longest"
    )
    return inputs

